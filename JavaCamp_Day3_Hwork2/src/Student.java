
public class Student extends User {
	private int percent;
	private String courseName;
	private String programsOfCourse;
	public int getPercent() {
		return percent;
	}
	public void setPercent(int percent) {
		this.percent = percent;
	}
	public String getCourseName() {
		return courseName;
	}
	public void setCourseName(String courseName) {
		this.courseName = courseName;
	}
	public String getProgramsOfCourse() {
		return programsOfCourse;
	}
	public void setProgramsOfCourse(String programsOfCourse) {
		this.programsOfCourse = programsOfCourse;
	} 
	

}
